package com.hienao.openlist2strm.handler;

/**
 * 文件类型枚举
 *
 * <p>定义了系统支持的文件类型。
 *
 * @author hienao
 * @since 2024-01-01
 */
public enum FileType {

  /** 视频文件 */
  VIDEO,

  /** NFO 文件 */
  NFO,

  /** 图片文件 */
  IMAGE,

  /** 字幕文件 */
  SUBTITLE,

  /**
   * 所有类型
   *
   * <p>用于表示处理器支持所有文件类型。
   */
  ALL
}
